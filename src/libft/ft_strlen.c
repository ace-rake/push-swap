#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	len;

	if (!s || s[0] == '\0')
		return (0);
	len = 0;
	while (*s != '\0')
	{
		len++;
		++s;
	}
	return (len);
}
